package com.svedentsov.aqa.tasks.strings;

/**
 * Решение задачи №18: Посчитать количество слов в строке.
 * Описание: Написать функцию, которая подсчитывает количество слов в предложении.
 * (Проверяет: работа со строками, split())
 * Задание: Напишите метод `int countWords(String sentence)`, который подсчитывает
 * количество слов в строке `sentence`. Слова разделены одним или несколькими пробелами.
 * Пример: `countWords("This is a sample sentence.")` -> `5`,
 * `countWords(" Hello   world ")` -> `2`.
 */
public class CountWordsString {

    /**
     * Подсчитывает количество слов в строке.
     * Словами считаются последовательности символов, разделенные одним или
     * несколькими пробельными символами (\s+).
     * Ведущие и завершающие пробелы игнорируются.
     *
     * @param sentence Входная строка (предложение). Может быть null.
     * @return Количество слов в строке. Возвращает 0, если строка null,
     * пуста или состоит только из пробельных символов.
     */
    public int countWords(String sentence) {
        // Обработка null или пустой строки
        if (sentence == null || sentence.isEmpty()) {
            return 0;
        }

        // Удаляем ведущие и завершающие пробелы
        String trimmedSentence = sentence.trim();

        // Если после trim строка стала пустой (содержала только пробелы)
        if (trimmedSentence.isEmpty()) {
            return 0;
        }

        // Разбиваем строку по одному или нескольким пробельным символам
        // \\s+ означает один или более пробельных символов (пробел, таб, \n, \r и т.д.)
        String[] words = trimmedSentence.split("\\s+");

        // Количество элементов в массиве - это количество слов
        return words.length;
    }
}
